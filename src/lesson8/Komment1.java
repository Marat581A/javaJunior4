package lesson8;

public class Komment1 {
    private String kom;
    private int like;

    public Komment1() {
    }

    public Komment1(String kom, int like) {
        this.kom = kom;
        this.like = like;
    }

    public String getKom() {
        return kom;
    }

    public void setKom(String kom) {
        this.kom = kom;
    }

    public int getLike() {
        return like;
    }

    public void setLike(int like) {
        this.like = like;
    }
}
